#include<iostream>

int main()
{
    std::cout<<"Size of integer: "<<sizeof(int)<<"bytes"<<"\n";
    std::cout<<"Size of float: "<<sizeof(float)<<"bytes"<<"\n";
    std::cout<<"Size of character: "<<sizeof(char)<<"bytes"<<"\n";
    std::cout<<"Size of Double Integer: "<<sizeof(double)<<"bytes"<<"\n";

    return 0;;
}
